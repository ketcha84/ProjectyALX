package J25;

public class Calculator {
    public int suma(int a, int b) {
        return a + b;
    }

    public int suma(int a, int b, int c) {
        return a + b + c;
    }

    public double suma(double a, double b) {
        return a + b;
    }

    public double suma(double a, double b, double c) {
        return a + b + c;
    }
}
